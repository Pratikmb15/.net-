using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOP
{
    internal class Sum
    {
        public int FindSum(int num1,int num2) {
            return num1+num2;
        }
        public double FindSum(double num1, double num2)
        {
            return num1+num2;
        }
        public double FindSum(double num1, double num2,double num3)
        {
            return num1 + num2 + num3;
        }

    }
}
